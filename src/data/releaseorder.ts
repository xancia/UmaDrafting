// Helper function to get release index by outfit ID (lower = newer)
export function getReleaseIndex(outfitId: string): number {
  return releaseOrder.findIndex((entry) => entry.outfitId === outfitId);
}

// Helper function to get release index by name and variant
export function getReleaseIndexByNameVariant(
  name: string,
  variant: string,
): number {
  return releaseOrder.findIndex(
    (entry) => entry.name === name && entry.variant === variant,
  );
}

// Helper function to check if a character exists in the release order
export function existsInReleaseOrder(outfitId: string): boolean {
  return getReleaseIndex(outfitId) !== -1;
}

// Helper function to compare two characters by release order (for sorting)
// Returns negative if a is newer, positive if b is newer
export function compareByReleaseOrder(
  aOutfitId: string,
  bOutfitId: string,
): number {
  const aIndex = getReleaseIndex(aOutfitId);
  const bIndex = getReleaseIndex(bOutfitId);

  // If not found, put them at the end
  if (aIndex === -1 && bIndex === -1) return 0;
  if (aIndex === -1) return 1;
  if (bIndex === -1) return -1;

  return aIndex - bIndex;
}

// Get entry by outfit ID
export function getEntryByOutfitId(outfitId: string): ReleaseEntry | undefined {
  return releaseOrder.find((entry) => entry.outfitId === outfitId);
}

export default releaseOrder;
